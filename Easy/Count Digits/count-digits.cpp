class Solution{
public:
    int evenlyDivides(int N){
        //code here
        string ss=to_string(N);
        int count=0;
        for(char c:ss){
            int lD=c-'0'; // char to int
            if(lD!=0 && N%lD==0){
                count+=1;
            }
        }
        return count;
    }
};

